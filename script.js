let virender = document.getElementById('virender');
virender.addEventListener('click', function () {
  imagevirendra.style.display = 'block';
  imagevirendra.style.transform = 'translate(5em, -20em)';
  imagevirendra.onmouseleave = () => {
    imagevirendra.style.display = 'none';
  };
});

